/**
 * Função para somar dois números
 * 
 * @param {number} num1 - O primeiro número a ser somado
 * @param {number} num2 - O segundo número a ser somado
 * @returns {number} - A soma dos dois números
 * @author PHPablo
 */
function somar(num1, num2) {

  //realiza a soma dos dois números
  return num1 + num2;
}

// Exemplo de uso
let soma = somar(3, 4);
console.log('=====================')
console.log(`A soma é: ${soma}`); // Saída esperada: 7
console.log('=====================')

// Exemplo de uso 2
let soma2 = somar(120, 3360);
console.log(`A soma é: ${soma2}`); // Saída esperada: 3480
console.log('=====================')
