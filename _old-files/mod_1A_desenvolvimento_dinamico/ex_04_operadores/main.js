// Operadores em JavaScript

// 1. Operadores Aritméticos
let a = 10;
let b = 5;
let soma = a + b; // Soma
let subtracao = a - b; // Subtração
let multiplicacao = a * b; // Multiplicação
let divisao = a / b; // Divisão
let resto = a % b; // Resto da divisão (módulo)

// 2. Operadores de Atribuição
let c = 3;
c += 2; // c = c + 2 (Agora c é 5)
c *= 2; // c = c * 2 (Agora c é 10)

// 3. Operadores de Comparação
let maiorQue = a > b; // Verifica se a é maior que b
let menorQue = a < b; // Verifica se a é menor que b
let igual = a == b; // Verifica se a é igual a b

// 4. Operadores Lógicos
let condicao1 = true;
let condicao2 = false;
let resultado = condicao1 && condicao2; // E lógico (false)
let resultado2 = condicao1 || condicao2; // OU lógico (true)

// Imprimir resultados
// Imprimir resultados com explicação
console.log('Soma (a + b):', soma); // Exibe o resultado da soma de a e b
console.log('Subtração (a - b):', subtracao); // Exibe o resultado da subtração de a e b
console.log('Multiplicação (a * b):', multiplicacao); // Exibe o resultado da multiplicação de a e b
console.log('Divisão (a / b):', divisao); // Exibe o resultado da divisão de a por b
console.log('Resto da divisão (a % b):', resto); // Exibe o resto da divisão de a por b

console.log('Valor de c após atribuições:', c); // Exibe o valor de c após as operações de atribuição

console.log('a > b:', maiorQue); // Exibe o resultado da comparação "a > b"
console.log('a < b:', menorQue); // Exibe o resultado da comparação "a < b"
console.log('a == b:', igual); // Exibe o resultado da comparação "a == b"

console.log('Resultado de condicao1 && condicao2:', resultado); // Exibe o resultado de E lógico (condicao1 && condicao2)
console.log('Resultado de condicao1 || condicao2:', resultado2); // Exibe o resultado de OU lógico (condicao1 || condicao2)


